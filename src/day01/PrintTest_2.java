package day01;

public class PrintTest_2 {
	//Ctrl + Spacebar : 자동완성
	//main 작성 후 자동완성
	public static void main(String[] args) {
		System.out.print(26);
		System.out.print("\n해진\n");
		
//		\n은 Enter의 의미입니다.
		System.out.print("\\n은 Enter의 의미입니다.\n");
		
//		"코리아 아이티 아카데미"
		System.out.println("\"코리아 아이티 아카데미\"");
		
		System.out.println('A');
		
//		syso 작성 후 자동완성 :println() 자동완성
//		수식은 결과값 하나로 봐야한다.(수식은 값이다!)
		System.out.println(3+5);
		System.out.println(8);
		
//		어떤 값이든 문자열과 + 를 하게 되면 연결이 일어난다.
		System.out.println(10 + "살");
		
//		1 + 1 = 2
		System.out.println(1 + "+" + 1 + "=" + 2);
		
	}
}
